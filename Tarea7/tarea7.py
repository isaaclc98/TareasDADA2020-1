#4 funciones basicas en python (suma,resta, multiplicacion y division)
def add(num1,num2):
    return num1 + num2

def substract(num1,num2):
    return num1 - num2

def multiply(num1,num2):
    return num1 * num2

def divide(num1,num2):
    return num1 / num2

print(add(3,5))
print(substract(10,4))
print(multiply(7,-4))
print(divide(45,5))
